#include "../include/libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*temp;

	if (!lst || !f || !del)
		return (NULL);
	new = ft_lstnew(f(lst ->content));
	if (!new)
		return (NULL);
	temp = new;
	while (lst && lst ->next)
	{
		if (!temp)
			return (NULL);
		temp -> next = ft_lstnew(f(lst -> next -> content));
		temp = temp -> next;
		lst = lst -> next;
	}
	return (new);
}
