#include "../include/libft.h"

static int	ft_in_set(const char *set, char c)
{
	while (*set)
	{
		if (*set == c)
			return (1);
		set++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s2;
	size_t	start;
	size_t	end;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_in_set(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_in_set(set, s1[end - 1]))
		end--;
	len = end - start;
	s2 = ft_substr(s1, start, len);
	return (s2);
}
